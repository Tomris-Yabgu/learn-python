# Lambda ile ilgili örnekler
#  The lambda fucktion's make many examples


#Addition / toplama
say =  lambda say1,say2:say1+say2
print(say(3,4))

#Multiplication/çarpma
say =  lambda say1,say2:say1*say2
print(say(3,4))

#Division / bölme
say =  lambda say1,say2:say1/say2
print(say(12,4))

#Subtraction /çıkarma
say =  lambda say1,say2:say1-say2
print(say(12,4))

#Modulus/mod almak /moduler işlem
say =  lambda say1,say2:say1%say2
print(say(12,4))

#pow / kuvvet,üst işlemleri
say =  lambda say1,say2:say1**say2
print(say(5,4))